/**
  Transliteration from Russian into English.
*/
let transliteration = function(text) {
  if (isBlank(text)) {
    return text;
  }

  let newText = '';
  for (let i = 0; i < text.length; i++) {
    let symbol = text.charAt(i);
    if (symbol === ' ') {
      newText += '_';
    } else if (!isNone(dictionary[symbol])) {
      newText += dictionary[symbol];
    }
  }

  return newText;
};

